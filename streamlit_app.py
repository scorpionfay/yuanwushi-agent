"""
streamlit_app.py — Web interface for the 元吾氏 AI 助教 (powered by Google Gemini).

Each browser session gets its own independent chat history.
Requires teacher_files.json to be present (generated by running agent.py locally first).
"""

import json
import os
import streamlit as st
from google import genai
from google.genai import types

FILES_DB = "teacher_files.json"
TEACHER_NAME = "元吾氏"
MODEL = "gemini-2.5-flash"
MAX_PAGE_TEXT_CHARS = 3000
MAX_PAGES_IN_CONTEXT = 30
MAX_PDF_TEXT_CHARS = 100_000


def build_system_instruction(state: dict) -> str:
    teacher_name = state.get("teacher_name", TEACHER_NAME)
    website_url = state.get("website_url", "")
    page_texts: list[dict] = state.get("page_texts", [])
    pdf_texts: list[dict] = state.get("pdf_texts", [])

    text = f"""你就是{teacher_name}老师本人。学生正在向你请教问题，你要完全以老师本人的身份、口吻和视角来回答，就像在课堂上或办公室里亲自辅导学生一样。

角色要求：
- 用第一人称"我"来表达，比如"我在课上讲过……""我的意思是……""你可以翻翻我给你们的讲义第X页"
- 保持老师本人的语气和风格，不要表现出你是AI
- 根据课程材料（PDF讲义、课程网站）来回答，这些都是你自己准备的教学内容
- 如果学生问到材料之外的内容，以老师的口吻引导或给出你的判断
- 优先用中文回答，除非学生用其他语言提问
- 可以适当反问学生，帮助他们自己思考，而不只是直接给答案
- 不要说"根据材料""文档中提到"这类话——那是你自己写的内容，直接讲就好"""

    if pdf_texts:
        text += "\n\n## 课程讲义内容\n\n"
        for pdf in pdf_texts:
            text += f"### {pdf['filename']}\n{pdf['text'][:MAX_PDF_TEXT_CHARS]}\n\n"

    if page_texts:
        text += f"\n\n## 课程网站内容（来源: {website_url}）\n\n"
        for page in page_texts[:MAX_PAGES_IN_CONTEXT]:
            title = page.get("title", page["url"])
            content = page["text"][:MAX_PAGE_TEXT_CHARS]
            text += f"### {title}\n{content}\n\n"

    return text


@st.cache_resource
def load_resources():
    """Load state and initialize Gemini client once, shared across all users."""
    client = genai.Client(api_key=os.environ.get("GEMINI_API_KEY"))

    with open(FILES_DB, encoding="utf-8") as f:
        state = json.load(f)

    system_instruction = build_system_instruction(state)
    return state, client, system_instruction


def response_stream(client, system_instruction: str, history: list):
    """Generator yielding text chunks from Gemini streaming response."""
    stream = client.models.generate_content_stream(
        model=MODEL,
        contents=history,
        config=types.GenerateContentConfig(
            system_instruction=system_instruction,
        ),
    )
    for chunk in stream:
        if chunk.text:
            yield chunk.text


def main():
    st.set_page_config(page_title="元吾氏 AI助教", page_icon="📚")
    st.title("📚 元吾氏老师 AI助教")

    state, client, system_instruction = load_resources()
    pdf_count = len(state.get("pdf_texts", []))
    page_count = len(state.get("page_texts", []))
    st.caption(f"已加载 {pdf_count} 份讲义 + {page_count} 个课程网页")

    # Per-session state: history for API, messages for display
    if "history" not in st.session_state:
        st.session_state.history = []   # list of {role, parts: [{text}]}
        st.session_state.messages = []  # list of {role, content} for display

    # Render chat history
    for msg in st.session_state.messages:
        with st.chat_message(msg["role"]):
            st.markdown(msg["content"])

    user_input = st.chat_input("请输入你的问题…")
    if not user_input:
        return

    # Add to display and API history
    st.session_state.messages.append({"role": "user", "content": user_input})
    st.session_state.history.append({"role": "user", "parts": [{"text": user_input}]})

    with st.chat_message("user"):
        st.markdown(user_input)

    with st.chat_message("assistant"):
        try:
            response_text = st.write_stream(
                response_stream(client, system_instruction, st.session_state.history)
            )
        except Exception as e:
            st.error(f"出错了：{e}")
            st.session_state.messages.pop()
            st.session_state.history.pop()
            return

    st.session_state.messages.append({"role": "assistant", "content": response_text})
    st.session_state.history.append({"role": "model", "parts": [{"text": response_text}]})


if __name__ == "__main__":
    main()
